// Déclaration des variables x, y et z
let x = "1";
let y = "2";
let z = x + y;

// Affichage du type et de la valeur de chaque variable
console.log("Valeur de x:", x, "Type de x:", typeof x);
console.log("Valeur de y:", y, "Type de y:", typeof y);
console.log("Valeur de z:", z, "Type de z:", typeof z);

// Forcer le type de x et y à entier
x = parseInt(x);
y = parseInt(y);
z = x + y;

// Affichage du type et de la valeur de chaque variable
console.log("Valeur de x:", x, "Type de x:", typeof x);
console.log("Valeur de y:", y, "Type de y:", typeof y);
console.log("Valeur de z:", z, "Type de z:", typeof z);
